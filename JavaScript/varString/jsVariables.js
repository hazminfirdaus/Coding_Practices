let cityName;
cityName = "Kuala Lumpur"; // String data type

console.log("City Name:", cityName);
console.log("Type of City Name:", typeof cityName); // Outputs: string

cityName = "Kuala Terengganu"; // Reassigning a new string value

console.log("Updated City Name:", cityName);

cityName = 12345; // Changing to a Number data type

console.log("City Name as Number:", cityName);
console.log("Type of City Name:", typeof cityName); // Outputs: number

const cityNameConst = "George Town"; // Constant variable

console.log("Constant City Name:", cityNameConst);
console.log("Type of Constant City Name:", typeof cityNameConst); // Outputs: string

var cityPopulation = 1780000; // Number data type

console.log("City Population:", cityPopulation);
console.log("Type of City Population:", typeof cityPopulation); // Outputs: number

cityPopulation = 1800000; // Reassigning a new number value

console.log("Updated City Population:", cityPopulation);
