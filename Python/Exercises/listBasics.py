# Exercise 5: List Basics
# 1. Create a list of 5 numbers.
# 2. Print:
#  • The largest number
#  • The smallest number
#  • The average of the numbers

list = [1, 2, 3, 4, 5]
print(f"The largest number is {max(list)}.")
print(f"The smallest number is {min(list)}.")
print(f"The average number is {sum(list)/len(list)}.")